#Built-in Math Functions
#The min() and max() functions can be used to find the lowest or highest value in an iterable
x = min(5, 10, 25)
y = max(5, 10, 25)

print(x)
print(y)

#The abs()
x = abs(-7.25)
print(x)

#pow(x, y)
#Return the value of 4 to the power of 3 (same as 4 * 4 * 4)
x = pow(4, 3)
print(x)

#Math Module
import math
#math.sqrt()
x = math.sqrt(64)
print(x)

#math.ceil() & math.floor()
import math
x = math.ceil(1.4)
y = math.floor(1.4)
print(x) # returns 2
print(y) # returns 1

#math.pi
import math
x = math.pi
print(x)


