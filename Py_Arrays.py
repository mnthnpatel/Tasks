#Arrays
#Create an array containing car names
cars = ["Ford", "Volvo", "BMW"]
print(cars)


#get value of first array
x=cars[0]
print(x)

#modify first array
cars[0]="Toyota"
print(cars)

#length
x=len(cars)
print(x)



#looping array elements
#print each items in the cars array
for x in cars:
    print(x)

#add element
cars.append("Honda")
print(x)

#remove
cars.pop(1)
print(x)

#with remove()
cars.remove("Volvo")
print(x)
